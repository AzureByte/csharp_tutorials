using System;

namespace csharp_tutorials.src._03_Data_Types
{
    class _06_int
    {
        public static void Main()
        {
            int a = 35;
            Int16 b = 55;
            Int32 c = 75;
            Int64 d = 95;

            Console.WriteLine("value contained in the variable 'a' = " + a);
            Console.WriteLine();

            Console.WriteLine("value contained in the variable 'b' = " + b);
            Console.WriteLine();

            Console.WriteLine("value contained in the variable 'c' = " + c);
            Console.WriteLine();

            Console.WriteLine("value contained in the variable 'd' = " + d);
            Console.WriteLine();

            Console.WriteLine("minimum value that an 'int' data type can hold = " + int.MinValue);
            Console.WriteLine();

            Console.WriteLine("maximum value that an 'int' data type can hold = " + int.MaxValue);
            Console.WriteLine();

            Console.WriteLine("minimum value that an 'int16' data type can hold = " + Int16.MinValue);
            Console.WriteLine();

            Console.WriteLine("maximum value that an 'int16' data type can hold = " + Int16.MaxValue);
            Console.WriteLine();

            Console.WriteLine("minimum value that an 'int32' data type can hold = " + Int32.MinValue);
            Console.WriteLine();

            Console.WriteLine("maximum value that an 'int32' data type can hold = " + Int32.MaxValue);
            Console.WriteLine();

            Console.WriteLine("minimum value that an 'int64' data type can hold = " + Int64.MinValue);
            Console.WriteLine();

            Console.WriteLine("maximum value that an 'int64' data type can hold = " + Int64.MaxValue);
            Console.WriteLine();
        }
    }
}
